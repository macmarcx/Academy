"""
Tipo Numérico
"""
# Soma
soma = 2 + 2
print(f"A soma é: {soma}")

# Subtração
subtracao = 3 - 2
print(f"A subtração é: {subtracao}")

# Multiplicação
multiplica = 2 * 3
print(f"A multiplicação é: {multiplica}")

# Divisão com ponto flutuante
dividir = 6 / 2
print(f"A divisão é: {dividir}")

# Divisão com numero real
dividir = 6 // 2
print(f"A divisão é: {dividir}")

# Fatoração
fatorar = 5 % 2
print(f"A fatoração é: {fatorar}")

# Valor elevado ao quadrado
elevado = 2 ** 2
print(f"O número elevado ao quaddrado é: {elevado}")